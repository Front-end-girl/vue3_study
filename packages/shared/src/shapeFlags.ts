// 由于位运算符属于低级运算操作，所以运算速度也是最快
export const enum ShapeFlags {
  ELEMENT = 1,
  FUNCTIONAL_COMPONENT = 1 << 1, // 00000010
  STATEFUL_COMPONENT = 1 << 2, //00000100
  TEXT_CHILDREN = 1 << 3, // 00001000
  ARRAY_CHILDREN = 1 << 4, //
  SLOTS_CHILDREN = 1 << 5,
  TELEPORT = 1 << 6,
  SUSPENSE = 1 << 7,
  COMPONENT_SHOULD_KEEP_ALIVE = 1 << 8,
  COMPONENT_KEPT_ALIVE = 1 << 9,
  COMPONENT = ShapeFlags.STATEFUL_COMPONENT | ShapeFlags.FUNCTIONAL_COMPONENT, // 00000010|00000100=00000110
}
